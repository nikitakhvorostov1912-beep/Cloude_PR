def get_capabilities(config_name: str) -> dict[str, dict]:
    """Возвращает справочник модулей для указанной конфигурации 1С.

    Args:
        config_name: Название конфигурации (например, '1С:ERP',
            '1С:Комплексная автоматизация', '1С:Управление торговлей',
            '1С:Зарплата и управление персоналом').

    Returns:
        Словарь модулей с их описаниями, документами, отчётами,
        процессами и возможностями. Пустой словарь, если конфигурация
        не найдена.
    """
    return ERP_CAPABILITIES.get(config_name, {})


def get_all_configs() -> list[str]:
    """Возвращает список всех доступных конфигураций 1С.

    Returns:
        Список названий конфигураций.
    """
    return list(ERP_CAPABILITIES.keys())


def get_module_names(config_name: str) -> list[str]:
    """Возвращает список модулей (подсистем) для указанной конфигурации.

    Args:
        config_name: Название конфигурации.

    Returns:
        Список названий модулей. Пустой список, если конфигурация
        не найдена.
    """
    capabilities = get_capabilities(config_name)
    return list(capabilities.keys())


def find_document(config_name: str, document_name: str) -> list[str]:
    """Ищет модули, содержащие указанный документ.

    Args:
        config_name: Название конфигурации.
        document_name: Название документа (или его часть) для поиска.

    Returns:
        Список названий модулей, в которых найден документ.
    """
    results: list[str] = []
    capabilities = get_capabilities(config_name)
    search_lower = document_name.lower()

    for module_name, module_data in capabilities.items():
        for doc in module_data.get("documents", []):
            if search_lower in doc.lower():
                results.append(module_name)
                break

    return results


def get_config_summary(config_name: str) -> dict[str, int]:
    """Возвращает сводную статистику по конфигурации.

    Args:
        config_name: Название конфигурации.

    Returns:
        Словарь со счётчиками: modules, documents, reports, processes, features.
    """
    capabilities = get_capabilities(config_name)
    summary: dict[str, int] = {
        "modules": len(capabilities),
        "documents": 0,
        "reports": 0,
        "processes": 0,
        "features": 0,
    }

    for module_data in capabilities.values():
        summary["documents"] += len(module_data.get("documents", []))
        summary["reports"] += len(module_data.get("reports", []))
        summary["processes"] += len(module_data.get("processes", []))
        summary["features"] += len(module_data.get("features", []))

    return summary
