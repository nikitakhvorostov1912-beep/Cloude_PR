def rgb_hex(color: RGBColor) -> str:
    """Convert RGBColor (tuple) to 6-char hex string."""
    return f'{color[0]:02X}{color[1]:02X}{color[2]:02X}'

def set_cell_bg(cell, color: RGBColor):
    """Установить фоновый цвет ячейки таблицы."""
    tc = cell._tc
    tcPr = tc.get_or_add_tcPr()
    shd = OxmlElement('w:shd')
    shd.set(qn('w:val'), 'clear')
    shd.set(qn('w:color'), 'auto')
    shd.set(qn('w:fill'), rgb_hex(color))
    tcPr.append(shd)

def set_row_bg(row, color: RGBColor):
    for cell in row.cells:
        set_cell_bg(cell, color)

def add_page_break(doc):
    doc.add_paragraph().add_run().add_break(
        __import__('docx.enum.text', fromlist=['WD_BREAK']).WD_BREAK.PAGE
    )

def h1(doc, text):
    p = doc.add_heading(text, level=1)
    p.style.font.color.rgb = C_DARK_BLUE
    return p

def h2(doc, text):
    p = doc.add_heading(text, level=2)
    p.style.font.color.rgb = C_MID_BLUE
    return p

def h3(doc, text):
    p = doc.add_heading(text, level=3)
    return p

def note(doc, text, label="ℹ️ Важно", bg=C_YELLOW_BG):
    """Цветная подсказка-блок."""
    tbl = doc.add_table(rows=1, cols=1)
    tbl.style = 'Table Grid'
    cell = tbl.rows[0].cells[0]
    set_cell_bg(cell, bg)
    p = cell.paragraphs[0]
    run = p.add_run(f"{label}:  ")
    run.bold = True
    run.font.size = Pt(10)
    p.add_run(text).font.size = Pt(10)
    doc.add_paragraph()

def result_box(doc, text):
    note(doc, text, label="✅ Результат", bg=C_GREEN_BG)

def trap_box(doc, text):
    note(doc, text, label="⚠️ Ловушка", bg=C_TRAP_BG)

def req_table(doc, rows_data, col_widths=None):
    """Таблица реквизитов: 2 колонки (Реквизит | Значение)."""
    tbl = doc.add_table(rows=1 + len(rows_data), cols=2)
    tbl.style = 'Table Grid'
    tbl.alignment = WD_TABLE_ALIGNMENT.LEFT
    if col_widths:
        for i, w in enumerate(col_widths):
            for row in tbl.rows:
                row.cells[i].width = Cm(w)
    # шапка
    hdr = tbl.rows[0]
    set_row_bg(hdr, C_LIGHT_BLUE)
    for cell, txt in zip(hdr.cells, ["Реквизит", "Значение для примера"]):
        p = cell.paragraphs[0]
        run = p.add_run(txt)
        run.bold = True
        run.font.size = Pt(10)
        run.font.color.rgb = C_DARK_BLUE
    # данные
    for i, (field, val) in enumerate(rows_data):
        row = tbl.rows[i + 1]
        row.cells[0].paragraphs[0].add_run(field).font.size = Pt(10)
        vrun = row.cells[1].paragraphs[0].add_run(val)
        vrun.font.size = Pt(10)
        if i % 2 == 0:
            set_row_bg(row, RGBColor(0xF7, 0xF9, 0xFD))
    doc.add_paragraph()
    return tbl

def path_box(doc, path):
    p = doc.add_paragraph()
    run = p.add_run(f"📂 Путь в меню:  {path}")
    run.font.size = Pt(10)
    run.font.italic = True
    run.font.color.rgb = C_GRAY


