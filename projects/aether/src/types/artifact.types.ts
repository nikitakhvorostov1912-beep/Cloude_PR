export type ArtifactType =
  | 'protocol'
  | 'requirements'
  | 'risks'
  | 'glossary'
  | 'questions'
  | 'transcript'
  | 'development'
  | 'summary'
  | 'aggregated'
  | 'custom';

export interface Artifact {
  id: string;
  meetingId: string;
  type: ArtifactType;
  version: number;
  data: Record<string, unknown>;
  llmProvider: string;
  llmModel: string;
  tokensUsed: number;
  costUsd: number;
  createdAt: string;
}

