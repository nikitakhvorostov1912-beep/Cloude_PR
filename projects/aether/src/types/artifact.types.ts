// ─── Типы артефакта "На разработку" ───────────────────────────────────────────

export type DevTaskPriority = 'critical' | 'high' | 'medium' | 'low';
export type DevTaskStatus = 'ready' | 'requires_clarification' | 'deferred';

export interface DevTaskAction {
  step: number;
  action: string;
  detail?: string;
}

export interface DevTask {
  id: string;
  title: string;
  group: string;
  priority: DevTaskPriority;
  status: DevTaskStatus;
  description: string;
  actions: DevTaskAction[];
  input_data: string;
  expected_result: string;
  acceptance_criteria: string[];
  dependencies: string[];
  meeting_notes: Array<{
    speaker: string;
    quote: string;
    timestamp: string;
  }>;
}

export interface DevelopmentArtifactMetadata {
  meeting_date: string;
  participants: Array<{ name: string; role: string }>;
  systems: string[];
  overall_status: 'ready' | 'partial' | 'requires_clarification';
  summary: string;
}

export interface DevelopmentArtifactData {
  metadata: DevelopmentArtifactMetadata;
  tasks: DevTask[];
}
