def set_table_style(table):
    """Базовое оформление таблицы с границами."""
    tbl = table._tbl
    tblPr = tbl.tblPr if tbl.tblPr is not None else tbl._new_tblPr()
    borders = tblPr.find(qn('w:tblBorders'))
    if borders is None:
        borders = tblPr.makeelement(qn('w:tblBorders'), {})
        tblPr.append(borders)
    for edge in ('top', 'left', 'bottom', 'right', 'insideH', 'insideV'):
        el = borders.makeelement(qn(f'w:{edge}'), {
            qn('w:val'): 'single',
            qn('w:sz'): '4',
            qn('w:space'): '0',
            qn('w:color'): '000000',
        })
        borders.append(el)


def shade_row(row, color="D9E2F3"):
    """Заливка строки."""
    for cell in row.cells:
        shading = cell._element.get_or_add_tcPr()
        sh = shading.makeelement(qn('w:shd'), {
            qn('w:fill'): color,
            qn('w:val'): 'clear',
        })
        shading.append(sh)


def set_cell_text(cell, text, bold=False, size=10):
    cell.text = ""
    p = cell.paragraphs[0]
    run = p.add_run(str(text))
    run.bold = bold
    run.font.size = Pt(size)
    run.font.name = "Calibri"


def add_table_with_header(doc, headers, rows):
    table = doc.add_table(rows=1 + len(rows), cols=len(headers))
    table.alignment = WD_TABLE_ALIGNMENT.CENTER
    set_table_style(table)
    # Header
    hdr = table.rows[0]
    shade_row(hdr, "2F5496")
    for i, h in enumerate(headers):
        set_cell_text(hdr.cells[i], h, bold=True, size=10)
        for run in hdr.cells[i].paragraphs[0].runs:
            run.font.color.rgb = RGBColor(255, 255, 255)
    # Data
    for ri, row_data in enumerate(rows):
        row = table.rows[ri + 1]
        if ri % 2 == 0:
            shade_row(row, "F2F2F2")
        for ci, val in enumerate(row_data):
            set_cell_text(row.cells[ci], val, size=9)
    return table


def doc_setup(doc):
    style = doc.styles['Normal']
    font = style.font
    font.name = 'Calibri'
    font.size = Pt(11)
    for section in doc.sections:
        section.top_margin = Cm(2)
        section.bottom_margin = Cm(2)
        section.left_margin = Cm(2.5)
        section.right_margin = Cm(2)


